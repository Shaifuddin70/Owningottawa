      current += increment;
      if (current >= target) {
        current = target;
        clearInterval(timer);
      }

      if (target >= 1000000) {
        element.textContent = formatNumber(Math.floor(current));
      } else {
        element.textContent = Math.floor(current);
      }
    }, 20);
